<h3>Coaching Availability Calendar</h3>

<p>The following calendar allows you to override the coaching timeslots on a given day by setting your availability for that day.  You can either
    mark yourself as unavailible for the day or set a specific timeslot that is available that day.  Your clients will then be able to
    schedule only during your given timeslot for that day instead of the general timeslots set up on the previous tab.</p>
